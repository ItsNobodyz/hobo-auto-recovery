-- ─────────────────────────────────────────────────────────────────────────────
-- Ambient Vehicle Spawner  (requires OneSync on your FiveM server)
-- When enabled, the server spawns one parked civilian vehicle at every entry in
-- Config.ParkingSpots on resource start. A random fraction are flagged as repo
-- cases and added to the pool. The rest fill the parking lots as ambient scenery.
-- Set enabled = false if your server does not run OneSync.
--
-- Natural bounds (v1.4.2): with caps removed, the system self-regulates via
--   active_repo_cases ≈ #ParkingSpots × spawnChance × repoChance
-- Each spawned vehicle ALSO honors a per-entry 10 min – 2 hr expiry timer; on
-- expiry, the parking spot frees and re-rolls (spawn + repo chance), so the set
-- floats around the natural cap with constant churn rather than staying static.
-- ─────────────────────────────────────────────────────────────────────────────
Config.AmbientSpawn = {
    enabled         = true,
    repoEligibleMax = nil,    -- nil = uncapped (was 45)
    -- v1.5: dropped spawnChance 0.75 → 0.60 (lots feel less crammed full of
    -- our spawns; adoption picks up GTA cars that fill in naturally), bumped
    -- repoChance 0.12 → 0.15. With the 1037-spot map (~384 new spots in v1.5)
    -- × 0.60 spawn × 0.15 repo ≈ 93 repo cars worldwide. Per-lot of 6 spots
    -- ≈ 0.5 expected repo, same feel as before but the absolute count grew
    -- proportionally with the new spots.
    spawnChance     = 0.60,
    repoChance      = 0.15,
}

-- v1.4.4: short-interval retry for spots that miss the 75% spawn roll. Distinct
-- from Config.ReplacementDelay (which is the 60–300 s post-expiry cooldown before
-- a freshly-vacated spot rolls again). EmptySpotRetry applies in two cases:
--   • Startup: any spot that misses the initial 75% spawn roll
--   • Post-respawn: when RespawnAmbientAtSpot also misses the spawn roll after
--                   its ReplacementDelay window
-- Without this, spots that miss the spawn roll stay empty for the resource's
-- entire uptime, and the world drifts toward more and more dead spots.
Config.EmptySpotRetry = { min = 10, max = 300 }   -- seconds (10 s – 5 min)

-- v1.4.4: scenery (non-repo) vehicles get the same 10 min – 2 hr lifetime that
-- repo cars get via CasePool.expiresAt. Spots churn regardless of which type
-- spawned. Tuned via the same min/max range so behavior is consistent.
